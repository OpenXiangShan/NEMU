      def_INSTR_TAB("??????? ????? ????? 101 ????? ????? ??", vload);
      def_INSTR_TAB("??????? ????? ????? 110 ????? ????? ??", vload);
      def_INSTR_TAB("??????? ????? ????? 111 ????? ????? ??", vload);
